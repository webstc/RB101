def tricky_method_two(a_string_param, an_array_param)
  # the "=" reassigns the variable "an_array_parm", which is pointing to the same 
  # value as "my_array" after the method invocation... so after the call to "tricky_method_two"
  # "an_array_param" and "my_array" diverge in their assignment.  Whereas Array#<< mutates
  # the caller.  an_array_param and my_array are assigned to the same value.
  
  a_string_param << 'rutabaga'
  an_array_param = ['pumpkins', 'rutabaga']
end

my_string = "pumpkins"
my_array = ["pumpkins"]
tricky_method_two(my_string, my_array)

puts "My string looks like this now: #{my_string}"
puts "My array looks like this now: #{my_array}"



#5. use return in the method definition, and return the specified values to 
#  the my_string and my_array

def not_so_tricky_method(a_string_param, an_array_param)
  a_string_param += "rutabaga"
  an_array_param += ["rutabaga"]
  
  return a_string_param, an_array_param
end

my_string = "pumpkins"
my_array = ["pumpkins"]
my_string, my_array = not_so_tricky_method(my_string, my_array)

puts "My string looks like this now: #{my_string}"
puts "My array looks like this now: #{my_array}"

